

export class Propietario {
    id: number;
    foto: string;
    nombre: string;
    celular: number;
    correo: string; 



constructor(
    id: number,
    foto: string,
    nombre: string,
    celular: number,
    correo: string){
    this.id = id;
    this.foto = foto;
    this.nombre = nombre;
    this.celular = celular;
    this.correo = correo;
    }
}

